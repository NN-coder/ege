# Алгоритм вычисления функции F(n) задан следующими соотношениями
def F(n):
  if n == 1:
    return 1
  if n % 2 == 0:
    return n + F(n - 1)
  elif n > 1:
    return 2 * F(n - 2)
# Чему равно значение функции F(26)?
print(F(26))

