sum = 0
def F(n):
  global sum
  sum += 2*n
  print(2*n)
  if n > 1:
    sum += n - 5
    print(n - 5)
    F(n - 1)
    F(n - 2)
  return sum

for n in range(1000):
  if F(n) <= 5e5:
    sum = 0
  else:
    break

